CREATE OR REPLACE FUNCTION 
FN_CADASTRAR_ENDERECO(P_ID_USUARIO UUID, P_ID_CIDADE UUID, P_CEP VARCHAR(20), 
					P_LOGRADOURO TEXT, P_BAIRRO VARCHAR(50), P_NUMERO VARCHAR(20))
RETURNS BOOLEAN AS $$
BEGIN
	 
	INSERT INTO ENDERECO(
		ID_USUARIO, 
		ID_CIDADE, 
		CEP, 
		LOGRADOURO, 
		BAIRRO,
		NUMERO)
	VALUES(
		P_ID_USUARIO, 
		P_ID_CIDADE, 
		P_CEP,
		P_LOGRADOURO,
		P_BAIRRO,
		P_NUMERO);

	RETURN TRUE;

	EXCEPTION WHEN OTHERS THEN
		PERFORM FN_REPORT_ERRO('ERRO AO CADASTRAR ENDEREÇO', SQLSTATE, SQLERRM, 'FN_CADASTRAR_ENDERECO', 'ENDERECO', CURRENT_QUERY());
		RETURN FALSE;
END;
$$ LANGUAGE PLPGSQL;

