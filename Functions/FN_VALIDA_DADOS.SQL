CREATE OR REPLACE FUNCTION FN_VALIDA_DADOS(VALIDADOR TEXT, OPCAO INT)
RETURNS BOOLEAN AS $$
DECLARE OK BOOLEAN;
BEGIN

	IF(OPCAO = 1) THEN
		SELECT (VALIDADOR = CPF)
		INTO OK
		FROM USUARIOS
		WHERE CPF = VALIDADOR;
		
		IF OK IS NULL THEN
			OK := FALSE;
		END IF;
		
		RETURN OK;

	ELSIF(OPCAO = 2) THEN
		SELECT (VALIDADOR = EMAIL)
		INTO OK
		FROM USUARIOS
		WHERE EMAIL = VALIDADOR;
		
		IF OK IS NULL THEN
			OK := FALSE;
		END IF;
		
		RETURN OK;
	ELSE
		SELECT (VALIDADOR = LOGIN)
		INTO OK
		FROM AUTENTIFICACAO
		WHERE LOGIN = VALIDADOR;
		
		IF OK IS NULL THEN
			OK := FALSE;
		END IF; 
		
		RETURN OK;
	END IF;
	
	EXCEPTION WHEN OTHERS THEN
		PERFORM FN_REPORT_ERRO('ERRO AO VALIDAR DADOS', sqlstate, sqlerrm, 'FN_VALIDA_DADOS', 'USUARIOS/AUTENTIFICACAO', CURRENT_QUERY());
		RETURN FALSE;
END;
$$  LANGUAGE PLPGSQL