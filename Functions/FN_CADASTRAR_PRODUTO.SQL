CREATE OR REPLACE FUNCTION 
FN_CADASTRAR_PRODUTO(P_ID_USUARIO UUID, P_NOME TEXT, P_DESCRICAO TEXT, 
					P_VALOR_BASE_DIARIA NUMERIC(16,2),P_VALOR_BASE_MENSAL NUMERIC(16,2),
					P_VALOR_PRODUTO NUMERIC(16,2), P_DATA_COMPRA DATE)
RETURNS BOOLEAN AS $$
BEGIN
	 
	INSERT INTO PRODUTOS(
		ID_USUARIO, 
		NOME, 
		DESCRICAO, 
		VALOR_BASE_DIARIA, 
		VALOR_BASE_MENSAL,
		VALOR_PRODUTO, 
		DATA_COMPRA)
	VALUES(
		P_ID_USUARIO, 
		P_NOME, 
		P_DESCRICAO,
		P_VALOR_BASE_DIARIA,
		P_VALOR_BASE_MENSAL,
		P_VALOR_PRODUTO,
		P_DATA_COMPRA);
																				  
	RETURN TRUE;

	EXCEPTION WHEN OTHERS THEN
		PERFORM FN_REPORT_ERRO('ERRO AO CADASTRAR PRODUTO', SQLSTATE, SQLERRM, 'FN_CADASTRAR_PRODUTO', 'PRODUTOS', CURRENT_QUERY());
		RETURN FALSE;
END;
$$ LANGUAGE PLPGSQL;

