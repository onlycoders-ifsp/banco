CREATE OR REPLACE FUNCTION FN_INSERIR_LOGIN(P_ID_USUARIO TEXT, P_SENHA TEXT, P_ID_LOGIN VARCHAR(50))
RETURNS BOOLEAN AS $$
BEGIN

	IF EXISTS (SELECT ID_USUARIO FROM AUTENTIFICACAO WHERE ID_USUARIO = UUID(P_ID_USUARIO)) THEN
		BEGIN
			UPDATE AUTENTIFICACAO
			SET PASSWORD = CRYPT(P_SENHA,CONCAT(gen_salt('bf',10),TO_CHAR(CURRENT_TIMESTAMP,'YYYYMMDDHHMMSSMSUS')))
			WHERE ID_USUARIO = UUID(P_ID_USUARIO);
			
			RETURN TRUE;
			
		EXCEPTION WHEN OTHERS THEN
			PERFORM FN_REPORT_ERRO('ERRO AO ATUALIZAR LOGIN', sqlstate, sqlerrm, 'FN_INSERIR_LOGIN', 'AUTENTIFICACAO', CURRENT_QUERY(),P_ID_LOGIN);
			RETURN FALSE;
		END;

	ELSE
		INSERT INTO AUTENTIFICACAO(ID_USUARIO, PASSWORD, LOGIN)
		VALUES(UUID(P_ID_USUARIO), CRYPT(P_SENHA,CONCAT(gen_salt('bf',10),TO_CHAR(CURRENT_TIMESTAMP,'YYYYMMDDHHMMSSMSUS'))), P_ID_LOGIN);
		RETURN TRUE;
	END IF;
	
	EXCEPTION WHEN OTHERS THEN
		PERFORM FN_REPORT_ERRO('ERRO AO INSERIR LOGIN', sqlstate, sqlerrm, 'FN_INSERIR_LOGIN', 'AUTENTIFICACAO', CURRENT_QUERY(),P_ID_LOGIN);
		RETURN FALSE;
END;
$$  LANGUAGE plpgsql