CREATE OR REPLACE FUNCTION FN_REPORT_ERRO(P_DESCRICAO TEXT, P_STATE TEXT, P_ERRO TEXT, P_PROCEDURE TEXT, P_TABELA TEXT, P_QUERY TEXT)
RETURNS VOID AS $$
BEGIN						  
	RAISE INFO '%', P_DESCRICAO;
	RAISE NOTICE '% %', P_STATE, P_ERRO;
	
	INSERT INTO LOG.LOG_ERROS(PROCEDURE, TABELA, QUERY, ERRO, STATE, DESCRICAO)
	VALUES (P_PROCEDURE, P_TABELA, P_QUERY, P_ERRO, P_STATE, P_DESCRICAO);
	
	EXCEPTION WHEN OTHERS THEN
		RAISE INFO 'ERRO EM FN_REPORT_ERRO % %', sqlstate, sqlerrm;
END;
$$  LANGUAGE plpgsql