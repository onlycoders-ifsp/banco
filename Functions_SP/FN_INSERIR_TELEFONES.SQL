CREATE OR REPLACE FUNCTION FN_INSERIR_TELEFONES(P_ID_USUARIO UUID, P_TELEFONE VARCHAR(20), P_CELULAR VARCHAR(20))
RETURNS BOOLEAN AS $$
DECLARE STATE TEXT;
DECLARE ERRO TEXT;
BEGIN
	INSERT INTO TELEFONES(ID_USUARIO, TELEFONE, CELULAR)
	VALUES(P_ID_USUARIO, P_TELEFONE, P_CELULAR);
	RETURN TRUE;
	
	EXCEPTION WHEN OTHERS THEN
		PERFORM FN_REPORT_ERRO('ERRO AO INSERIR TELEFONES', sqlstate, sqlerrm);
		RETURN FALSE;
END;
$$  LANGUAGE PLPGSQL