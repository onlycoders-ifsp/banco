CREATE TABLE TIPOS_CONTA(
	ID_TIPOS_CONTA UUID DEFAULT UUID_GENERATE_V4(),
	COD_TIPOS_CONTA INT NOT NULL UNIQUE,
	DESCRICAO_CONTA TEXT NOT NULL UNIQUE,
	DATA_INCLUSAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	DATA_MODIFICACAO TIMESTAMP,
	PRIMARY KEY(ID_TIPOS_CONTA)
);
	
CREATE UNIQUE INDEX IX_TBL_TIPOS_CONTA ON TIPOS_CONTA (COD_TIPOS_CONTA,DESCRICAO_CONTA);