CREATE TABLE TELEFONES(
	ID_TELEFONE UUID DEFAULT UUID_GENERATE_V4(),
	ID_USUARIO UUID REFERENCES USUARIOS(ID_USUARIO),
	TELEFONE TEXT[2] NOT NULL,
	FLAG_VERIFICADO BIT,
	DATA_INCLUSAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	DATA_MODIFICACAO TIMESTAMP,
	PRIMARY KEY(ID_TELEFONE),
	UNIQUE(ID_USUARIO, TELEFONE)
);

CREATE UNIQUE INDEX IX_TBL_TELEFONES ON TELEFONES (TELEFONE);