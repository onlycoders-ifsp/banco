CREATE TABLE TELEFONES(
	ID_TELEFONE UUID DEFAULT UUID_GENERATE_V4(),
	ID_USUARIO UUID REFERENCES USUARIOS(ID_USUARIO) ON DELETE CASCADE,
	TELEFONE VARCHAR(20) NULL UNIQUE,
	CELULAR VARCHAR(20) NULL UNIQUE,
	IS_VERIFICADO BOOLEAN NOT NULL DEFAULT FALSE,
	DATA_INCLUSAO TIMESTAMPTZ NOT NULL DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_TELEFONE)
);

CREATE UNIQUE INDEX IX_TBL_TELEFONES ON TELEFONES (TELEFONE,CELULAR,IS_VERIFICADO);