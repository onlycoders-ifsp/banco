CREATE TABLE PAIS(
	ID_PAIS UUID DEFAULT UUID_GENERATE_V4(),
	SIGLA CHAR(2) NOT NULL UNIQUE,
	NOME VARCHAR(50) NOT NULL,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ NULL,
	PRIMARY KEY(ID_PAIS)
);

CREATE UNIQUE INDEX IX_TBL_PAIS ON PAIS (ID_PAIS, SIGLA, NOME, DATA_INCLUSAO);

ALTER TABLE PAIS
ADD CONSTRAINT UPPER_CONSTRAINT
CHECK(
SIGLA = UPPER(SIGLA)
AND SUBSTRING(NOME,1,1) = UPPER(SUBSTRING(NOME,1,1))
)