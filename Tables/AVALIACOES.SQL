CREATE TABLE AVALIACOES(
	ID_AVALIACAO UUID DEFAULT UUID_GENERATE_V4(),
	ID_ALUGUEL UUID REFERENCES ALUGUEIS(ID_ALUGUEL),
	ID_PRODUTO UUID REFERENCES PRODUTOS(ID_PRODUTO),
	COMENTARIO TEXT NULL,
	NOTA DECIMAL(3,1) CHECK(NOTA < 11),
	ID_TIPO_AVALIACAO UUID REFERENCES TIPO_AVALIACAO(ID_TIPO_AVALIACAO),
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_TIPO_AVALIACAO)
);
	
CREATE UNIQUE INDEX IX_TBL_AVALIACOES ON AVALIACOES (ID_AVALIACAO, ID_ALUGUEL, ID_USUARIO_AVALIADOR, COMENTARIO, NOTA, DATA_INCLUSAO);