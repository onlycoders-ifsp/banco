CREATE TABLE IDIOMAS(
	ID_IDIOMA UUID DEFAULT UUID_GENERATE_V4(),
	COD_IDIOMA INT NOT NULL UNIQUE,
	ABREVIATURA_IDIOMA VARCHAR(5) NOT NULL UNIQUE,
	NOME VARCHAR(50) NOT NULL,
	DESCRICAO TEXT NOT NULL,
	DATA_INCLUSAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	DATA_MODIFICACAO TIMESTAMP,
	PRIMARY KEY(ID_IDIOMA)
);
	
CREATE UNIQUE INDEX IX_TBL_IDIOMAS ON IDIOMAS (COD_IDIOMA,ABREVIATURA_IDIOMA,NOME,DESCRICAO);