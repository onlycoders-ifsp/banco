CREATE TABLE STATUS_PROBLEMA(
	ID_STATUS_PROBLEMA UUID DEFAULT UUID_GENERATE_V4(),
	COD_STATUS_PROBLEMA INT NOT NULL UNIQUE,
	DESCRICAO TEXT NOT NULL UNIQUE,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_STATUS_PROBLEMA)
);
	
CREATE UNIQUE INDEX IX_TBL_STATUS_PROBLEMA ON STATUS_PROBLEMA (DESCRICAO);