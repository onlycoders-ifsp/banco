CREATE TABLE LOG.LOG_ERROS(
	ID SERIAL PRIMARY KEY,
	PROCEDURE TEXT NOT NULL,
	TABELA TEXT,
	ERRO TEXT,
	STATE TEXT,
	QUERY TEXT,
	DESCRICAO TEXT,
	DATA_ERRO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP)
);

CREATE UNIQUE INDEX IX_TBL_LOG_ERROS on LOG.LOG_ERROS(id,procedure,tabela,data_erro)