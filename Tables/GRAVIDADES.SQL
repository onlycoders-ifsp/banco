CREATE TABLE GRAVIDADES(
	ID_GRAVIDADE UUID DEFAULT UUID_GENERATE_V4(),
	COD_GRAVIDADE INT NOT NULL UNIQUE,
	DESCRICAO TEXT NOT NULL UNIQUE,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', timezone('America/Sao_Paulo', CURRENT_TIMESTAMP)),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_GRAVIDADE)
);
	
CREATE UNIQUE INDEX IX_TBL_GRAVIDADES ON GRAVIDADES (ID_GRAVIDADE, COD_GRAVIDADE,DESCRICAO, DATA_INCLUSAO);