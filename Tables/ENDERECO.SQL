CREATE TABLE ENDERECO(
	ID_ENDERECO UUID DEFAULT UUID_GENERATE_V4(),
	ID_USUARIO UUID REFERENCES USUARIO(ID_USUARIO),
	ID_CIDADE UUID REFERENCES CIDADE(ID_CIDADE),
	CEP VARCHAR(20) NULL,
	LOGRADOURO TEXT NOT NULL,
	BAIRRO VARCHAR(50) NULL,
	NUMERO VARCHAR(20) NULL,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ NULL,
	PRIMARY KEY(ID_CIDADE)
);

CREATE UNIQUE INDEX IX_TBL_ENDERECO ON ENDERECO (ID_ENDERECO, ID_USUARIO, ID_CIDADE, CEP, LOGRADOURO, NUMERO, DATA_INCLUSAO);