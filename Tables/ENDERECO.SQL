CREATE TABLE ENDERECO(
	ID_ENDERECO UUID DEFAULT UUID_GENERATE_V4(),
	ID_USUARIO UUID REFERENCES USUARIOS(ID_USUARIO),
	ESTADO TEXT,
	CIDADE TEXT,
	CEP VARCHAR(20) NULL,
	LOGRADOURO TEXT NULL,
	COMPLEMENTO VARCHAR(20),
	BAIRRO VARCHAR(50) NULL,
	NUMERO VARCHAR(20) NULL,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ NULL,
	PRIMARY KEY(ID_ENDERECO)
);

CREATE UNIQUE INDEX IX_TBL_ENDERECO ON ENDERECO (ID_ENDERECO, ID_USUARIO, ESTADO, CIDADE, CEP, LOGRADOURO, BAIRRO, DATA_INCLUSAO);