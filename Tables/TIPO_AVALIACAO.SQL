CREATE TABLE TIPO_AVALIACAO(
	ID_TIPO_AVALIACAO UUID DEFAULT UUID_GENERATE_V4(),
	COD_TIPO_AVALIACAO INT NOT NULL UNIQUE,
	DESCRICAO TEXT NOT NULL UNIQUE,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_TIPO_AVALIACAO)
);
	
CREATE UNIQUE INDEX IX_TBL_TIPO_AVALIACAO ON TIPO_AVALIACAO (COD_TIPO_AVALIACAO,DESCRICAO);