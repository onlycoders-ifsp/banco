CREATE TABLE TIPO_CONTA(
	ID_TIPO_CONTA UUID DEFAULT UUID_GENERATE_V4(),
	COD_TIPO_CONTA INT NOT NULL UNIQUE,
	DESCRICAO_CONTA TEXT NOT NULL UNIQUE,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_TIPO_CONTA)
);
	
CREATE UNIQUE INDEX IX_TBL_TIPO_CONTA ON TIPO_CONTA (COD_TIPO_CONTA,DESCRICAO_CONTA);