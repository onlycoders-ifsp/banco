CREATE TABLE TIPO_PROBLEMA(
	ID_TIPO_PROBLEMA UUID DEFAULT UUID_GENERATE_V4(),
	COD_TIPO_PROBLEMA INT NOT NULL UNIQUE,
	DESCRICAO TEXT NOT NULL UNIQUE,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_TIPO_PROBLEMA)
);

CREATE UNIQUE INDEX IX_TBL_TIPO_PROBLEMA ON TIPO_PROBLEMA (COD_TIPO_PROBLEMA,DESCRICAO);