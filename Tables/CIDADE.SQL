CREATE TABLE CIDADE(
	ID_CIDADE UUID DEFAULT UUID_GENERATE_V4(),
	ID_ESTADO UUID REFERENCES ESTADO(ID_ESTADO),
	NOME VARCHAR(50) NOT NULL,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ NULL,
	PRIMARY KEY(ID_CIDADE)
);

CREATE UNIQUE INDEX IX_TBL_CIDADE ON CIDADE (ID_CIDADE, ID_ESTADO, NOME, DATA_INCLUSAO);