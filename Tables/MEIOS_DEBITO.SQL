CREATE TABLE MEIOS_DEBITO(
	ID_DEBITO UUID DEFAULT UUID_GENERATE_V4(),
	ID_USUARIO UUID REFERENCES USUARIOS(ID_USUARIO),
	AGENCIA VARCHAR(10) NOT NULL,
	CONTA VARCHAR(15) NOT NULL,
	ID_BANCO UUID REFERENCES BANCOS(ID_BANCO),
	ID_TIPO_CONTA UUID REFERENCES TIPOS_CONTA(ID_TIPOS_CONTA),
	DATA_INCLUSAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	DATA_MODIFICACAO TIMESTAMP,
	PRIMARY KEY(ID_DEBITO),
	UNIQUE(AGENCIA,CONTA,ID_TIPO_CONTA)
);
	
CREATE UNIQUE INDEX IX_TBL_MEIOS_DEBITOS ON MEIOS_DEBITO (AGENCIA,CONTA);