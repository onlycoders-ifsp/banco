CREATE TABLE public.MEIOS_DEBITO(
	ID_DEBITO UUID DEFAULT public.UUID_GENERATE_V4(),
	ID_USUARIO UUID REFERENCES public.USUARIOS(ID_USUARIO) ON DELETE CASCADE,
	AGENCIA VARCHAR(10) NOT NULL,
	CONTA VARCHAR(15) NOT NULL,
	BANCO VARCHAR(50) NOT NULL,
	COD_TIPO_CONTA INT REFERENCES PUBLIC.TIPO_CONTA(COD_TIPO_CONTA),
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_DEBITO),
	UNIQUE(AGENCIA,CONTA,COD_TIPO_CONTA)
);
	
CREATE UNIQUE INDEX IX_TBL_MEIOS_DEBITOS ON public.MEIOS_DEBITO (ID_DEBITO, ID_USUARIO, AGENCIA, CONTA, DATA_INCLUSAO);