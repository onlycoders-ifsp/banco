CREATE TABLE ESTADO(
	ID_ESTADO UUID DEFAULT UUID_GENERATE_V4(),
	ID_PAIS UUID REFERENCES PAIS(ID_PAIS),
	SIGLA CHAR(2) NOT NULL UNIQUE,
	NOME VARCHAR(50) NOT NULL,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ NULL,
	PRIMARY KEY(ID_ESTADO)
);

CREATE UNIQUE INDEX IX_TBL_ESTADO ON ESTADO (ID_ESTADO, ID_PAIS, SIGLA, NOME, DATA_INCLUSAO);