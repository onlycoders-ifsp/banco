CREATE TABLE BANCOS(
	ID_BANCO UUID DEFAULT UUID_GENERATE_V4(),
	COD_BANCO VARCHAR(10) NOT NULL,
	NOME_BANCO TEXT NOT NULL,
	DATA_INCLUSAO TIMESTAMPTZ DEFAULT timezone('America/Sao_Paulo', CURRENT_TIMESTAMP),
	DATA_MODIFICACAO TIMESTAMPTZ,
	PRIMARY KEY(ID_BANCO),
	UNIQUE(COD_BANCO, NOME_BANCO)
);
	
CREATE UNIQUE INDEX IX_TBL_BANCOS ON BANCOS (ID_BANCO, COD_BANCO, NOME_BANCO, DATA_INCLUSAO);